package net.prokhyon.modularfuzzy.fuzzySet.view.drawing;

public class FuzzySetPointDrawCoordinate {

	private Double xPoint;
	private Double yPoint;

	public FuzzySetPointDrawCoordinate(Double xPoint, Double yPoint) {
		super();
		this.xPoint = xPoint;
		this.yPoint = yPoint;
	}

	public Double getxPoint() {
		return xPoint;
	}

	public Double getyPoint() {
		return yPoint;
	}

}
